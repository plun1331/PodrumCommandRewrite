"""
This is a replacement file for the existing CommandManager.py. 
Two functions have been added, getCommand(), and deleteCommand().
"""


class CommandManager:
    commands = {}

    @staticmethod
    def isCommand(command):
        if command in CommandManager.commands:
            return True
        return False
    
    @staticmethod
    def registerCommand(command):
        CommandManager.commands[command.name] = command

    @staticmethod
    def deleteCommand(command):
        if command in CommandManager.commands:
            del(CommandManager.commands[command])

    @staticmethod
    def getCommand(command):
        if command in CommandManager.commands:
            return CommandManager.commands[command]
        return None
